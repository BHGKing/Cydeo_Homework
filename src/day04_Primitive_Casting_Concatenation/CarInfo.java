package day04_Primitive_Casting_Concatenation;

public class CarInfo {
    public static void main(String[] args) {

        int year = 2018;

        String makeModel = "Toyota Camry",
                 miles = "50,000 miles",
                  color = "Red",
                   price = "$19,000";

        System.out.println("Car information is: ");
        System.out.println("\t" + year + " " + makeModel + ", " + miles + ", " + color + ", " + price);




    }
}
