package day06_Intro_If_Statements;

public class MonthName {
    public static void main(String[] args) {

        int num = 10;
        String month = " ";

        if (num == 1){
            month = "January";
        }
        if (num == 2){
            month = "February";
        }
        if (num == 3){
            month = "March";
        }
        if (num == 4){
            month = "April";
        }
        if (num == 5){
            month = "May";
        }
        if (num == 6){
            month = "June";
        }
        if (num == 7){
            month = "July";
        }
        if (num == 8){
            month = "August";
        }
        if (num == 9){
            month = "September";
        }
        if (num == 10){
            month = "October";
        }
        if (num == 11){
            month = "November";
        }
        if (num == 12){
            month = "December";
        }
        System.out.println(month);
    }
}
